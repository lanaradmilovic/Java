    private void popuniSponzore() {
        try {
            ArrayList<Sponzor> sponzori = ClientController.getInstance().getAllSponzor();

            cmbSponzor.removeAllItems();

            for (Sponzor sponzor : sponzori) {
                cmbSponzor.addItem(sponzor);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void srediFormu() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
        txtDatumVremePocetka.setText(sdf.format(t.getDatumVremePocetka()));
        popuniSponzore();
        cmbSponzor.getModel().setSelectedItem(t.getSponzor());
        cmbPocetnoOdrediste.getModel().setSelectedItem(t.getPocetnoOdrediste());
        cmbKrajnjeOdrediste.getModel().setSelectedItem(t.getKrajnjeOdrediste());
        cmbPocetnoOdrediste.setEnabled(false);
        cmbKrajnjeOdrediste.setEnabled(false);
        txtOpis.setText(t.getOpis());
        tblPrijave.setModel(new TableModelPrijave(t));
        popuniUcesnike();

        if (!t.getDatumVremePocetka().after(new Date())) {
            JOptionPane.showMessageDialog(this, "Ova trka je prosla, ne mozete"
                    + " je menjati!");
            txtDatumVremePocetka.setEditable(false);
            txtNapomena.setEditable(false);
            txtOpis.setEditable(false);
            cmbSponzor.setEnabled(false);
            cmbUcesnik.setEnabled(false);
            btnDodaj.setEnabled(false);
            btnObrisiPrijavu.setEnabled(false);
            btnObrisi.setEnabled(false);
            btnIzmeni.setEnabled(false);
        }

    }

    private void popuniUcesnike() {
        try {
            ArrayList<Ucesnik> ucesnici = ClientController.getInstance().getAllUcesnik();

            cmbUcesnik.removeAllItems();

            for (Ucesnik ucesnik : ucesnici) {
                cmbUcesnik.addItem(ucesnik);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


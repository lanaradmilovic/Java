    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovu trku?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteTrka(t);
                FormPretragaTrke fp = (FormPretragaTrke) getParent();
                fp.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno obrisana trka.");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiTrke.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        if (txtDatumVremePocetka.getText().isEmpty() 
                || txtOpis.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }

        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
            Date DatumVremePocetka = sdf.parse(txtDatumVremePocetka.getText());
            String opis = txtOpis.getText();
            Sponzor sponzor = (Sponzor) cmbSponzor.getSelectedItem();

            TableModelPrijave tm = (TableModelPrijave) tblPrijave.getModel();

            t.setDatumVremePocetka(DatumVremePocetka);
            t.setOpis(opis);
            t.setSponzor(sponzor);
            t.setPrijave(tm.getLista());

            ClientController.getInstance().updateTrke(t);
            FormPretragaTrke fp = (FormPretragaTrke) getParent();
            fp.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjena trka.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(FormDetaljiPutnika.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

